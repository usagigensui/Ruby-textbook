# 2.9.1

# 2進数は0bを先頭につける
0b11111111 #=> 255

# 8進数は0を先頭につける
0377 #=> 255

# 16進数は0xを先頭につける
0xff #=> 255

# 2.9.2

# ビット演算
(~0b1010).to_s(2) #=> "-1011"
(0b1010 & 0b1100).to_s(2) #=> "1000"
(0b1010 | 0b1100).to_s(2) #=> "1110"

# 2.9.3

# 指数表現
2e-3 #=> 0.002

# 2.9.4

# 整数はInteger型
10.class #=> Integer

# 少数はFloat型
1.5.class #=> Float