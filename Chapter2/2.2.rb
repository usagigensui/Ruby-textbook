# 2.2.8

# 変数は何かしらを代入する必要がある(宣言するだけの構文はない)
x = nil

# 変数名は小文字のスネークケースで書く(キャメルケースは使わない)
  special_price = 200
  _special_price = 200 # アンダースコアから始めることもできる
  special_price_2 = 300
  # 数字から始まる変数名は使えずエラーする

# アルファベット以外の変数名も使える(不具合の原因になりかねないので普通はしない)
特別価格 = 200

# ふたつの値を同時に代入する
  a, b = 1, 2
  a #=> 1
  b #=> 2

# 右辺が少ない場合はnilが入る
  c, d = 10
  c #=> 10
  d #=> nil

# 右辺が多い場合ははみ出した値が切り捨てられる
  e, f = 100, 200, 300
  e #=> 100
  f #=> 200
